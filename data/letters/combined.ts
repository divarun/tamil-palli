export const vowelOrder = ["அ", "ஆ", "இ", "ஈ", "உ", "ஊ", "எ", "ஏ", "ஐ", "ஒ", "ஓ", "ஔ"];
export const consonantOrder = ["க", "ச", "ட", "த", "ப", "ற", "ங", "ஞ", "ண", "ந", "ம", "ன", "ய", "ர", "ல", "வ", "ழ", "ள"];

export function getCombined(consonant: string, vowel: string): string {
  return combinedForms[consonant]?.[vowel] ?? `${consonant}${vowelMarkers[vowel] ?? ""}`;
}

export function getAllCombined(): CombinedLetter[] {
  const result: CombinedLetter[] = [];
  for (const c of consonantOrder) {
    for (const v of vowelOrder) {
      result.push({
        tamil: getCombined(c, v),
        consonant: c,
        vowel: v,
        romanization: `${consonants.find(x => x.tamil === c)?.romanization ?? c}${vowels.find(x => x.tamil === v)?.romanization ?? v}`,
      });
    }
  }
  return result;
}

